class DetailInfo {
  final String address, name;
  final double occupancy0,
      occupancy20_0,
      occupancy20_1,
      occupancy20_2,
      occupancy40_0,
      occupancy40_1,
      occupancy40_2,
      occupancy60_0,
      occupancy60_1,
      occupancy60_2,
      occupancy120_0,
      occupancy120_1,
      occupancy120_2,
      charAvg20,
      charAvg40,
      charAvg60,
      charAvg120,
      charAvg4;
  final int sid;

  DetailInfo.fromJson(dynamic json)
      : address = json['station']['address'],
        name = json['station']['name'],
        sid = json['station']['sid'],
        occupancy0 = json['status']['occupancy0'] ?? 0.0,
        occupancy20_0 = json['status']['occupancy20_0'] ?? 0.0,
        occupancy20_1 = json['status']['occupancy20_1'] ?? 0.0,
        occupancy20_2 = json['status']['occupancy20_2'] ?? 0.0,
        occupancy40_0 = json['status']['occupancy40_0'] ?? 0.0,
        occupancy40_1 = json['status']['occupancy40_1'] ?? 0.0,
        occupancy40_2 = json['status']['occupancy40_2'] ?? 0.0,
        occupancy60_0 = json['status']['occupancy60_0'] ?? 0.0,
        occupancy60_1 = json['status']['occupancy60_1'] ?? 0.0,
        occupancy60_2 = json['status']['occupancy60_2'] ?? 0.0,
        occupancy120_0 = json['status']['occupancy120_0'] ?? 0.0,
        occupancy120_1 = json['status']['occupancy120_1'] ?? 0.0,
        occupancy120_2 = json['status']['occupancy120_2'] ?? 0.0,
        charAvg20 = json['charAvg20'] ?? 0.0,
        charAvg40 = json['charAvg40'] ?? 0.0,
        charAvg60 = json['charAvg60'] ?? 0.0,
        charAvg120 = json['charAvg120'] ?? 0.0,
        charAvg4 = json['charAvg4'] ?? 0.0;

  DetailInfo({
    required this.address,
    required this.name,
    required this.sid,
    required this.occupancy0,
    required this.occupancy20_0,
    required this.occupancy20_1,
    required this.occupancy20_2,
    required this.occupancy40_0,
    required this.occupancy40_1,
    required this.occupancy40_2,
    required this.occupancy60_0,
    required this.occupancy60_1,
    required this.occupancy60_2,
    required this.occupancy120_0,
    required this.occupancy120_1,
    required this.occupancy120_2,
    required this.charAvg120,
    required this.charAvg20,
    required this.charAvg40,
    required this.charAvg60,
    required this.charAvg4,
  });
}
